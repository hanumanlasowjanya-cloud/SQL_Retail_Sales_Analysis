-- Employee Analysis (3 Questions)
-- Highest Revenue Employee
SELECT EMPLOYEENUMBER,SUM(QUANTITYORDERED*PRICEEACH) AS TOTAL_REVENUE
FROM EMPLOYEES JOIN CUSTOMERS ON  EMPLOYEES.EMPLOYEENUMBER=CUSTOMERS.SALESREPEMPLOYEENUMBER
JOIN ORDERS USING (CUSTOMERNUMBER) JOIN ORDERDETAILS USING (ORDERNUMBER)
GROUP BY EMPLOYEENUMBER
ORDER BY TOTAL_REVENUE DESC
LIMIT 1;


-- Highest Revenue Office
SELECT OFFICECODE,SUM(QUANTITYORDERED*PRICEEACH) AS TOTAL_REVENUE
FROM OFFICES JOIN EMPLOYEES  USING (OFFICECODE) JOIN CUSTOMERS ON  EMPLOYEES.EMPLOYEENUMBER=CUSTOMERS.SALESREPEMPLOYEENUMBER
JOIN ORDERS USING (CUSTOMERNUMBER) JOIN ORDERDETAILS USING (ORDERNUMBER)
GROUP BY OFFICECODE
ORDER BY TOTAL_REVENUE DESC
LIMIT 1;

-- Revenue by Office
