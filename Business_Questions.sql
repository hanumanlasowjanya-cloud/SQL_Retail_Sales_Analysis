SELECT SUM(QUANTITYORDERED*PRICEEACH) AS TOTAL_REVENUE 
FROM ORDERS JOIN ORDERDETAILS USING (ORDERNUMBER);

SELECT  COUNTRY,SUM(QUANTITYORDERED*PRICEEACH) AS TOTAL_REVENUE 
FROM CUSTOMERS JOIN ORDERS USING (CUSTOMERNUMBER) JOIN ORDERDETAILS USING (ORDERNUMBER)
GROUP BY COUNTRY;


SELECT PRODUCTLINE,SUM(QUANTITYORDERED*PRICEEACH) AS TOTAL_REVENUE 
FROM ORDERDETAILS JOIN PRODUCTS USING (PRODUCTCODE)
GROUP BY PRODUCTLINE;

SELECT CUSTOMERNUMBER,SUM(QUANTITYORDERED*PRICEEACH) AS TOTAL_REVENUE 
FROM ORDERS JOIN ORDERDETAILS USING (ORDERNUMBER)
GROUP BY CUSTOMERNUMBER
ORDER BY TOTAL_REVENUE DESC
LIMIT 10;

-- Highest Paying Customer
SELECT CUSTOMERNUMBER,SUM(AMOUNT) AS TOTAL_AMOUNT
FROM CUSTOMERS JOIN PAYMENTS USING (CUSTOMERNUMBER)
GROUP BY CUSTOMERNUMBER
ORDER BY TOTAL_AMOUNT DESC
LIMIT 1;


-- Average Customer Payment
SELECT CUSTOMERNUMBER,AVG(AMOUNT) AS AVERAGE_AMOUNT
FROM CUSTOMERS JOIN PAYMENTS USING (CUSTOMERNUMBER)
GROUP BY CUSTOMERNUMBER
ORDER BY AVERAGE_AMOUNT  DESC
LIMIT 1;


-- Customers Above Country Average
WITH CTE1 AS (SELECT CUSTOMERNUMBER,SUM(AMOUNT) OVER(PARTITION BY CUSTOMERNUMBER) AS TOTAL_AMOUNT_CUSTOMERS
FROM CUSTOMERS JOIN PAYMENTS USING (CUSTOMERNUMBER)
)
SELECT * FROM CTE1 WHERE TOTAL_AMOUNT_CUSTOMERS > (SELECT AVG(TOTAL_AMOUNT_CUSTOMERS)
FROM CTE1);
-- Customers Without Orders
SELECT CUSTOMERNUMBER
FROM CUSTOMERS LEFT JOIN ORDERS USING (CUSTOMERNUMBER)
WHERE ORDERNUMBER IS NULL;


-- Customers with Most Orders
SELECT CUSTOMERNUMBER,COUNT(ORDERNUMBER) AS COUNTER
FROM CUSTOMERS JOIN ORDERS USING (CUSTOMERNUMBER)
GROUP BY CUSTOMERNUMBER
ORDER BY COUNTER DESC ;




-- Find the best-selling product based on total quantity sold.
SELECT PRODUCTCODE,PRODUCTNAME,SUM(QUANTITYORDERED) AS TOTAL_QUANTITY
FROM ORDERDETAILS JOIN PRODUCTS USING (PRODUCTCODE)
GROUP BY PRODUCTCODE,PRODUCTNAME
ORDER BY TOTAL_QUANTITY DESC
LIMIT 1;

-- Find the lowest-selling product based on total quantity sold.
SELECT PRODUCTCODE,PRODUCTNAME,SUM(QUANTITYORDERED) AS TOTAL_QUANTITY
FROM ORDERDETAILS JOIN PRODUCTS USING (PRODUCTCODE)
GROUP BY PRODUCTCODE,PRODUCTNAME
ORDER BY TOTAL_QUANTITY ASC
LIMIT 1;

-- Find the product with the highest revenue in each product line.
SELECT * FROM (SELECT PRODUCTLINE,PRODUCTCODE,PRODUCTNAME,SUM(QUANTITYORDERED*PRICEEACH) AS TOTAL_REVENUE,
ROW_NUMBER() OVER(PARTITION BY PRODUCTLINE ORDER BY  SUM(QUANTITYORDERED*PRICEEACH) DESC) AS RN
FROM ORDERDETAILS JOIN PRODUCTS USING (PRODUCTCODE)
GROUP BY PRODUCTLINE,PRODUCTCODE,PRODUCTNAME)T12
WHERE RN=1;

-- Find the average revenue generated by each product line.
