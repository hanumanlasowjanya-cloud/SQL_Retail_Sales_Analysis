;

-- Top 3 Customers in Each Country by Revenue
WITH CTE AS (SELECT CUSTOMERNUMBER, CUSTOMERNAME,COUNTRY,SUM(QUANTITYORDERED * PRICEEACH) AS TOTAL_REVENUE
FROM CUSTOMERS JOIN ORDERS USING (CUSTOMERNUMBER) JOIN ORDERDETAILS USING (ORDERNUMBER)
GROUP BY CUSTOMERNUMBER, CUSTOMERNAME, COUNTRY
)
SELECT * FROM (SELECT CUSTOMERNUMBER, CUSTOMERNAME, COUNTRY, TOTAL_REVENUE,
ROW_NUMBER() OVER (PARTITION BY COUNTRY ORDER BY TOTAL_REVENUE DESC) AS RN
FROM CTE) T
WHERE RN <= 3;


-- Rank Products by Revenue within each Product Line
WITH CTE AS (SELECT PRODUCTLINE,PRODUCTCODE,PRODUCTNAME,SUM(QUANTITYORDERED * PRICEEACH) AS TOTAL_REVENUE
FROM ORDERDETAILS JOIN PRODUCTS USING (PRODUCTCODE)
GROUP BY PRODUCTLINE, PRODUCTCODE, PRODUCTNAME
)
SELECT PRODUCTLINE,PRODUCTCODE,PRODUCTNAME,TOTAL_REVENUE,
ROW_NUMBER() OVER (PARTITION BY PRODUCTLINE ORDER BY TOTAL_REVENUE DESC) AS PRODUCT_RANK
FROM CTE;
